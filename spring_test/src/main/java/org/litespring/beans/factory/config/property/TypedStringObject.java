package org.litespring.beans.factory.config.property;

public class TypedStringObject {
	private String value;

	public TypedStringObject(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}


	
	
}
